    <h1 style="color: brown;">Papeda telor ayam</h1>
    <p>Papeda telor ayam. Kapurung (Papeda) Makanan khas Papua ini berbahan dasar telur ayam. Ya masyarakat Papua sering menambahkan rempah khas Papua pada olahan ayam pedasnya ini. Sebetulnya tidak harus memakai bumbu balado, kamu bisa ganti Cara Membuat Papeda Telur.</p>
    <h2>Bahan :</h2>
<ol>
<li> 1 butir telor ayam.</li>
<li> kaldu bubuk dan garam.</li>
<li> lada bubuk.</li>
<li> 5 sendok tepung sagu.</li>
<li> mentega untuk olesan teflon.</li>
<li> 5 tusukan sate.</li>
<li> 5 sendok sayur air.</li>
<li> bubuk boncabe jika suka/bisa skip .</li>
</ol>
<h3>Cara memasak :</h3>
<ol>
<li> Kocok lepas telor dengan sedikit garam dan lada bubuk</li>
<li> Campur tepung, air dan garam, aduk-aduk bila terlalu kental bisa ditambah air lagi.</li>
<li> Nyalakan api kecil dan taruh wajan teflon ukuran 23 cm di atasnya, oleskan mentega.</li>
<li> Tuang setengah sendok sayur telor dan goyang sebentar agar telor tidak menggumpal.</li>
<li> Tambahkan 1 sendok sayur adonan tepung, jika sudah agak mengering taburkan kaldu bubuk & cabe bubuk, langsung gulung dengan tusukan sate.</li>
<li> Ulangi sampai adonan habis. Siap dihidangkan dengan cocolan saus atau mayonaise.</li>
</ol>

