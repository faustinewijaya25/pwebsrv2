    <h1 style="color: brown;">Rawon Surabaya</h1>
    <p>Resep rawon daging sapi bisa kamu terapkan sebagai sajian Idul Adha. Rawon merupakan hidangan berkuah seperti sup yang berisi potongan lembut daging serta aroma dan warna hitam khas dari buah keluak. Rawon merupakan makanan khas dari Jawa Timur. Rasanya yang begitu menggugah selera membuatnya digemari banyak orang. Bahkan tidak hanya orang Surabaya atau Jawa Timur saja yang gemar menyantapnya, orang-orang dari luar Pulau Jawa, bahkan orang asing dari luar negeri pun menyukainya.</p>
Resep rawon daging sapi yang enak juga cukup mudah dibuat. Kamu bisa menjadikan daging sapi kurban menjadi santapan yang lezat ini. Idealnya rawon dibuat dari daging sandung lamur atau di dunia kuliner dikenal dengan brisket, yang sedikit berlemak dan enak dimasak lama. Namun, kamu juga bisa menggunakan bagian-bagian lainnya sesuai selera.

    <h2>Bahan:</h2>
    <ol>
<li>500 gram daging sapi, bagian sandung lumur</li>
<li>Air untuk merebus daging</li>
<li>1.500 ml air kaldu sapi</li>
<li>3 sdm minyak goreng, untuk menumis</li>
<li>1 sdm garam</li>
<h3>Haluskan:</h3>
<li>6 siung bawang putih</li>
<li>4 biji keluak (ambil isinya, lalu rendam dalam air)</li>
<li>8 buah cabai merah</li>
<li>2 ibu jari lengkuas</li>
<li>3 cm kunyit</li>
<li>2 cm kencur</li>
<li>2 cm jahe</li>
<li>2 sdm ketumbar</li>
<li>1/4 sdt jintan</li>
<li>1/2 sdt merica butir</li>
<li>4 lembar daun salam</li>
<li>2 batang serai, memarkan</li>
<li>2 mata asam jawa, larutkan dengan 1 sdm air</li>
<li>5 lembar daun jeruk purut</li>
</ol>
<h3>Pelengkap:</h3>
<ol>
<li>Tauge pendek/kecambah</li>
<li>Telur bebek asin</li>
<li>Sambal terasi</li>
<li>Kerupuk ikan</li>
</ol>
<h3>Cara memasak :</h3>
<ol>
<li>Cuci daging sampai bersih, rebus sampai matang. Angkat dan tiriskan daging, lalu potong ukuran dadu. Sisihkan daging dan air kaldunya.</li>
<li>Panaskan minyak, tumis bumbu halus sampai harum dan matang. Masukkan daging, serai, daun jeruk, air asam, dan garam. Aduk sampai rata dan beraroma.</li>
<li>Pukul-pukul daging sampai melebar. Goreng dalam minyak panas sebentar, angkat. Taburi dengan bawang goreng</li>
</ol>

